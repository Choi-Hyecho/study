public class OperationEx {
    public static void main(String[] args) {
        // 대입 연산자 예시
        // 대입연산자를 사용하여 int 형식의 age 이름을 가신 변수에 24를 넣음
        int age = 24;
        System.out.println(age);


        //부호 연산자 예시
        int num =10;
        int num2= -num;
        System.out.println(num);
        System.out.println(num2);

        num = -num;
        System.out.println(num);
    }
}